    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        if (supplierIdTxt.getText().trim().isEmpty() || supplierIdTxt.isEditable()) {
            JOptionPane.showMessageDialog(this, "Search first before deleting!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete this supplier?",
            "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) return;

        SupplierDao dao = new SupplierDaoImpl();

        if (dao.delete(supplierIdTxt.getText().trim()) > 0) {
            JOptionPane.showMessageDialog(this, "Supplier deleted!");
            supplierIdTxt.setEditable(true);
            clearFields();
            loadSuppliersToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Delete failed!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        if (supplierIdTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Supplier ID is required!", "Validation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (supplierFirstNameTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "supplier name is required!", "Validation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!supplierFirstNameTxt.getText().trim().matches("^[A-Za-z ]+$")) {
    JOptionPane.showMessageDialog(this, "Name must contain only letters and spaces!", "VALIDATION",
        JOptionPane.ERROR_MESSAGE
    );
    return;
}
       
        if (supplierContactInformation.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phone is required!", "Validation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!supplierContactInformation.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Phone must contain only positive numbers!", "VALIDATION", JOptionPane.ERROR_MESSAGE);
    return;
    }
        if (supplierEmailTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email is required!", "Validation", JOptionPane.ERROR_MESSAGE);
            return;
        }
          if(supplierEmailTxt.getText().trim().isEmpty() || !supplierEmailTxt.getText().matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")){
         JOptionPane.showMessageDialog(this, "Valid Email is required!", "VALIDATION", JOptionPane.ERROR_MESSAGE);
        return;
    }

        if (addressTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address is required!", "Validation", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Supplier s = new Supplier();
        s.setSupplierId(supplierIdTxt.getText().trim());
        s.setSupplierNames(supplierFirstName.getText().trim());
        s.setSuppliercontact(supplierContactInformation.getText().trim());
        s.setSupplierEmail(supplierEmailTxt.getText().trim());
        s.setAddress(addressTxt.getText().trim());

        SupplierDao dao = new SupplierDaoImpl();

        if (dao.create(s) > 0) {
            JOptionPane.showMessageDialog(this, "Supplier saved successfully!");
            clearFields();

        } else {
            JOptionPane.showMessageDialog(this, "Failed to save supplier!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

