private void loadSuppliersToTable() {
    SupplierDao dao = new SupplierDaoImpl();
    java.util.List<Supplier> list = dao.displayAllSuppliers();

    String[] cols = {"ID", "First Name", "phone", "Email", "Address"};
    Object[][] data = new Object[list.size()][5];

    for (int i = 0; i < list.size(); i++) {
        Supplier s = list.get(i);
        data[i][0] = s.getSupplierId();
        data[i][1] = s.getSupplierNames();
         data[i][2] = s.getSuppliercontact();
        data[i][3] = s.getSupplierEmail();
        data[i][4] = s.getAddress();
    }

    jTable1.setModel(new javax.swing.table.DefaultTableModel(data, cols));
}

