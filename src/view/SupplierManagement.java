    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        if (supplierIdTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Search before updating!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (supplierIdTxt.isEditable()) {
            JOptionPane.showMessageDialog(this, "Search first. ID must be locked.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Supplier s = new Supplier();
        s.setSupplierId(supplierIdTxt.getText().trim());
        s.setSupplierNames(supplierFirstNameTxt.getText().trim());
        s.setSuppliercontact(supplierContactInformation.getText().trim());
        s.setSupplierEmail(supplierEmail.getText().trim());
        s.setAddress(addressTxt.getText().trim());

        SupplierDao dao = new SupplierDaoImpl();

        if (dao.update(s) > 0) {
            JOptionPane.showMessageDialog(this, "Supplier updated successfully!");
            supplierIdTxt.setEditable(true);
            clearFields();
            loadSuppliersToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Update failed!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String id = supplierIdTxt.getText().trim();

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Supplier ID to search!", "Validation", JOptionPane.WARNING_MESSAGE);
            return;
        }

        SupplierDao dao = new SupplierDaoImpl();
        Supplier s = dao.searchByCode(id);

        if (s == null) {
            JOptionPane.showMessageDialog(this, "Supplier not found!", "Not Found", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
            supplierIdTxt.setEditable(true);
            return;
        }

        supplierIdTxt.setText(s.getSupplierId());
        supplierFirstNameTxt.setText(s.getSupplierNames());
        supplierContactInformation.setText(s.getSuppliercontact());
        supplierEmailTxt.setText(s.getSupplierEmail());

        addressTxt.setText(s.getAddress());

        supplierIdTxt.setEditable(false);
    }//GEN-LAST:event_searchBtnActionPerformed

